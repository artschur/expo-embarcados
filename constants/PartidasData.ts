export function formatHorario(inicio: string, fim: string) {
  const inicioDate = new Date(inicio);
  const fimDate = new Date(fim);

  const dia = inicioDate.toLocaleDateString('pt-BR', {
    day: '2-digit',
    month: '2-digit',
    year: 'numeric',
  });
  const horaInicio = inicioDate.toLocaleTimeString('pt-BR', {
    hour: '2-digit',
    minute: '2-digit',
  });
  const horaFim = fimDate.toLocaleTimeString('pt-BR', {
    hour: '2-digit',
    minute: '2-digit',
  });

  return `${dia} • ${horaInicio} às ${horaFim}`;
}
